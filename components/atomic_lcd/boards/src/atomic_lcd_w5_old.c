esp_err_t a_lcd_w5(a_lcd_t *lcd, bool (*cb)(esp_lcd_panel_io_handle_t, esp_lcd_panel_io_event_data_t *, void *))
{
	info(TAG, "a_lcd_w5()");
	esp_lcd_panel_handle_t	  panel		= NULL;
	esp_lcd_panel_io_handle_t io_handle = NULL;

	info(TAG, "initializing spi bus host: %d, channel: %d", LCD_SPI_HOST, SPI_DMA_CH_AUTO);
	try(spi_bus_initialize(LCD_SPI_HOST, &a_spi_bus_cfg, SPI_DMA_CH_AUTO));
	a_spi_io_cfg.on_color_trans_done = cb;
	try(esp_lcd_new_panel_io_spi(LCD_SPI_HOST, &a_spi_io_cfg, &io_handle));

	try(esp_lcd_new_panel_axs15231b(io_handle, &a_dev_cfg, &panel));
    try(esp_lcd_panel_reset(panel));
    vTaskDelay(pdMS_TO_TICKS(200));
    try(esp_lcd_panel_init(panel));
    try(esp_lcd_panel_disp_on_off(panel, false));

	atomic_gpio_config_out(a_lcd_gpio_cfg.bl, false, false);
	atomic_gpio_set(a_lcd_gpio_cfg.bl, 1);

	lcd->id		   = LCD_W5;
	lcd->panel	   = panel;
	lcd->cfg	   = &a_lcd_cfg;
	lcd->io_handle = io_handle;

    info(TAG, "a_lcd_w5() display done.");
	return ESP_OK;
}